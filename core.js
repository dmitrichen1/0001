// sessionId → { messages, startTime, paused, warmCount, warmTimer, noWarm,
//               clientName, clientPhone, clientUsername,
//               managerNotify: fn(text, clientId, session, opts) }
const sessions = {};

function getSession(sessionId) {
  if (!sessions[sessionId]) {
    sessions[sessionId] = {
      messages: [],
      startTime: new Date(),
      paused: false,
      warmCount: 0,
      warmTimer: null,
      noWarm: false,
      clientName: null,
      clientPhone: null,
      clientUsername: null,
      systemOverlay: '',  // доп. промпт канала (лендинг и т.п.)
      // Адаптер обязан задать эту функцию после создания сессии
      // fn(type, payload) — см. константы NOTIFY_* ниже
      notify: null
    };
  }
  return sessions[sessionId];
}

// Типы уведомлений — адаптер смотрит на type и делает что нужно
const NOTIFY = {
  SUMMARY:        'summary',        // передать сводку менеджеру
  CALC_RESULT:    'calc_result',    // расчёт готов (для менеджера: раскрой+прибыль)
  ALERT:          'alert',          // ALERT_MANAGER: горячий/грубый/сложный
  SEND_PRODUCTION:'send_production',// показать фото производства клиенту
  SEND_CARE:      'send_care',      // памятка по уходу клиенту
  SEND_EDGES:     'send_edges',     // фото кромок клиенту (type: 'kvarc'/'akril')
  CALC_ERROR:     'calc_error'      // ошибка расчёта
};

// ─── Вызов Claude API ──────────────────────────────────────────────────────
// systemOverlay — доп. текст к промпту в зависимости от канала (напр. лендинг)
function askClaude(messages, systemOverlay = '') {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({
      model: 'claude-sonnet-4-6',
      max_tokens: 1000,
      system: SYSTEM + (systemOverlay || ''),
      messages
    });

    const req = https.request({
      hostname: 'api.anthropic.com',
      path: '/v1/messages',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': ANTHROPIC_KEY,
        'anthropic-version': '2023-06-01',
        'Content-Length': Buffer.byteLength(body)
      }
    }, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try {
          const parsed = JSON.parse(data);
          resolve(parsed.content?.[0]?.text || 'Что-то пошло не так.');
        } catch (e) { reject(e); }
      });
    });

    req.on('error', reject);
    req.write(body);
    req.end();
  });
}

// ─── Подогрев молчащих клиентов ───────────────────────────────────────────
const WARM_DELAYS_MS = [2 * 60 * 60 * 1000, 24 * 60 * 60 * 1000];

function scheduleWarmUp(sessionId, session) {
  if (session.warmTimer) clearTimeout(session.warmTimer);
  if (session.noWarm || session.paused) return;
  if (session.warmCount >= WARM_DELAYS_MS.length) return;

  const delay = WARM_DELAYS_MS[session.warmCount];
  session.warmTimer = setTimeout(async () => {
    if (session.noWarm || session.paused) return;
    session.warmCount++;
    try {
      const warmMsgs = session.messages.concat([{
        role: 'user',
        content: '[СИСТЕМА: клиент молчит уже некоторое время после твоего вопроса. Напомни о себе одним коротким ненавязчивым сообщением, чтобы оживить диалог. Не повторяй прежние формулировки.]'
      }]);
      const reply = await askClaude(warmMsgs, session.systemOverlay);
      const clean = reply.replace(/\[STAGE:\d+\]/g, '').replace(/SEND_\w+/g, '').replace(/ALERT_MANAGER/g, '').trim();
      session.messages.push({ role: 'assistant', content: reply });
      if (session.notify && clean) session.notify('message', { text: clean });
      scheduleWarmUp(sessionId, session);
    } catch (e) {
      console.error('Ошибка подогрева:', e);
    }
  }, delay);
}

// ─── Обработка тегов и расчёт сметы ──────────────────────────────────────
async function processTags(sessionId, session, rawReply) {
  const events = []; // { type, payload } — адаптер разберёт

  // ALERT_MANAGER
  if (rawReply.includes('ALERT_MANAGER')) {
    session.paused = true;
    events.push({ type: NOTIFY.ALERT, payload: { sessionId, session, rawReply } });
  }

  // SEND_PRODUCTION
  if (rawReply.includes('SEND_PRODUCTION')) {
    events.push({ type: NOTIFY.SEND_PRODUCTION, payload: {} });
  }

  // SEND_CARE
  if (rawReply.includes('SEND_CARE')) {
    events.push({ type: NOTIFY.SEND_CARE, payload: {} });
  }

  // SEND_EDGES
  if (rawReply.includes('SEND_EDGES_AKRIL')) {
    events.push({ type: NOTIFY.SEND_EDGES, payload: { material: 'akril' } });
  } else if (rawReply.includes('SEND_EDGES')) {
    events.push({ type: NOTIFY.SEND_EDGES, payload: { material: 'kvarc' } });
  }

  // SEND_SUMMARY
  if (rawReply.includes('SEND_SUMMARY')) {
    events.push({ type: NOTIFY.SUMMARY, payload: { sessionId, session } });
  }

  // CALC — расчёт сметы
  let calcInsert = '';
  const calcMatch = rawReply.match(/\[?CALC\]?\s*(\{[\s\S]*?\})\s*\[?\/CALC\]?/);
  if (calcMatch) {
    try {
      const params = JSON.parse(calcMatch[1].trim());
      const est = await calc.estimateProject(params);
      if (est.error) {
        events.push({ type: NOTIFY.CALC_ERROR, payload: { sessionId, session, error: est.error } });
      } else {
        const lo = est.total[0], hi = est.total[1];
        let priceStr;
        if (est.priceFrom) {
          priceStr = `*от ${lo.toLocaleString('ru-RU')} ₽*`;
        } else if (lo === hi) {
          priceStr = `*${lo.toLocaleString('ru-RU')} ₽*`;
        } else {
          priceStr = `*${lo.toLocaleString('ru-RU')}–${hi.toLocaleString('ru-RU')} ₽*`;
        }
        calcInsert = priceStr;

        const priceClient = est.priceFrom ? `от ${lo}` : (lo === hi ? lo : `${lo}–${hi}`);
        events.push({
          type: NOTIFY.CALC_RESULT,
          payload: {
            sessionId,
            session,
            usedStone: est.usedStone,
            priceFrom: est.priceFrom,
            cutDesc: est.cut.desc,
            confidence: Math.round(est.confidence * 100),
            priceClient,
            profit: est.profit,
            profitPct: est.profitPct
          }
        });
      }
    } catch (e) {
      console.log('CALC parse fail:', e.message);
    }
  }

  // Чистый текст для клиента
  let clean = rawReply
    .replace(/\[STAGE:\d+\]/g, '')
    .replace(/\[?CALC\]?\s*\{[\s\S]*?\}\s*\[?\/CALC\]?/, calcInsert)
    .replace('SEND_SUMMARY', '')
    .replace('SEND_PRODUCTION', '')
    .replace('SEND_CARE', '')
    .replace('SEND_EDGES_AKRIL', '')
    .replace('SEND_EDGES', '')
    .replace('ALERT_MANAGER', '')
    .replace(/\|\|\|\s*$/, '')
    .trim();

  return { clean, events };
}

