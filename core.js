/**
 * handleMessage(sessionId, userMessage, options)
 *
 * options:
 *   imageBase64   string  — base64 изображения (если есть)
 *   imageMediaType string — 'image/jpeg' / 'image/png' / ...
 *   imageCaption  string  — подпись к изображению
 *   clientName    string  — имя из профиля канала
 *   clientUsername string — ник (@...)
 *
 * Возвращает:
 *   { parts: string[], events: [] }
 *   parts — массив частей ответа (разделённых |||), адаптер шлёт по очереди
 *   events — массив { type, payload }, адаптер обрабатывает по типу
 */
async function handleMessage(sessionId, userMessage, options = {}) {
  const session = getSession(sessionId);

  // Обогащаем сессию данными из канала
  if (options.clientName && !session.clientName) session.clientName = options.clientName;
  if (options.clientUsername && !session.clientUsername) session.clientUsername = options.clientUsername;
  if (options.systemOverlay) session.systemOverlay = options.systemOverlay;

  // Ловим телефон
  if (typeof userMessage === 'string') {
    const phoneM = userMessage.match(/\+?[78][\s\-]?\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2}/);
    if (phoneM) session.clientPhone = phoneM[0];
  }

  // Сбрасываем подогрев при активности клиента
  if (session.warmTimer) { clearTimeout(session.warmTimer); session.warmTimer = null; }
  session.warmCount = 0;

  if (session.paused) return { parts: [], events: [] };

  // Формируем сообщение для Claude
  let userContent;
  if (options.imageBase64) {
    userContent = [
      { type: 'image', source: { type: 'base64', media_type: options.imageMediaType || 'image/jpeg', data: options.imageBase64 } },
      { type: 'text', text: options.imageCaption || userMessage || 'Вот план/фото моего проекта.' }
    ];
  } else {
    userContent = typeof userMessage === 'string' ? userMessage : 'Привет.';
  }

  session.messages.push({ role: 'user', content: userContent });

  const rawReply = await askClaude(session.messages, session.systemOverlay);
  session.messages.push({ role: 'assistant', content: rawReply });

  const { clean, events } = await processTags(sessionId, session, rawReply);

  // Разбиваем по |||
  const parts = clean.split('|||').map(p => p.trim()).filter(Boolean);

  // Подогрев
  const phoneRe = /\+?[78][\s\-]?\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2}/;
  const callRe = /(по телефону|перезвон|наберите|позвон|голос)/i;
  const hasContact = (typeof userMessage === 'string' && (phoneRe.test(userMessage) || callRe.test(userMessage))) || rawReply.includes('SEND_SUMMARY');
  if (hasContact) {
    session.noWarm = true;
    if (session.warmTimer) { clearTimeout(session.warmTimer); session.warmTimer = null; }
  } else {
    scheduleWarmUp(sessionId, session);
  }

  return { parts, events };
}

/**
 * handleStart(sessionId, options)
 * Первое приветствие при /start или открытии чата
 * Возвращает { parts } или null (если сессия уже активна)
 */
function handleStart(sessionId, options = {}) {
  const session = getSession(sessionId);
  if (options.clientName && !session.clientName) session.clientName = options.clientName;
  if (options.clientUsername && !session.clientUsername) session.clientUsername = options.clientUsername;
  if (options.systemOverlay) session.systemOverlay = options.systemOverlay;

  if (session.messages.length > 0) return null; // не сбрасываем контекст

  const greeting = 'Добрый день. Смотрите столешницы — подбираете под конкретный проект или пока изучаете варианты?';
  session.messages.push({ role: 'assistant', content: greeting });
  return { parts: [greeting] };
}

/**
 * pauseSession / resumeSession — менеджер берёт/возвращает клиента
 */
function pauseSession(sessionId) {
  const s = getSession(sessionId);
  s.paused = true;
}

function resumeSession(sessionId) {
  const s = getSession(sessionId);
  s.paused = false;
}

/**
 * injectManagerMessage — менеджер пишет от имени бота
 * Записывает в историю клиента как assistant-сообщение
 */
function injectManagerMessage(sessionId, text) {
  const s = sessions[sessionId];
  if (s) s.messages.push({ role: 'assistant', content: text });
}

